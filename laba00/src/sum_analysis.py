def main():
    with open('input.txt', 'r') as file:  # O(n) - чтение всех строк файла
        lines = file.readlines()

    print("Содержимое файла:")  # O(1) - вывод константной строки
    for line in lines:  # O(n) - обход всех строк
        print(line.strip())  # O(1) - вывод одной строки

    a = int(lines[0].strip())  # O(1) - работа с первым элементом
    b = int(lines[1].strip())  # O(1) - работа со вторым элементом

    result = a + b  # O(1) - арифметическое сложение

    print(f"\nРезультат сложения: {a} + {b} = {result}")  # O(1)

    with open('output.txt', 'w') as output_file:  # O(1) - открытие файла
        output_file.write(str(result))  # O(1) - запись числа

    print(f"Результат сохранен в output.txt: {result}")  # O(1)


def alternative_version():
    """Альтернативная реализация с чтением из стандартного потока ввода."""
    a = int(input("Введите первое число: "))  # O(1) - чтение одной строки
    b = int(input("Введите второе число: "))  # O(1) - чтение одной строки
    result = a + b  # O(1) - арифметическое сложение
    print(f"Сумма: {result}")  # O(1) - вывод результата


if __name__ == "__main__":
    print("Запуск основной программы (чтение из input.txt):\n")  # O(1)
    main()  # O(n) - вызов основной функции
    print("\n" + "=" * 50)  # O(1) - вывод разделителя
    print("Альтернативная версия (ввод с клавиатуры):")  # O(1)
    alternative_version()  # O(1) - вызов альтернативной функции

